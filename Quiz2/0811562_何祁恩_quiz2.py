import pandas as pd
import matplotlib.pyplot as plt

def problem_1():
  ret = {}
  years = [2011, 2012, 2013, 2014, 2015, 2016, 2017, 2018, 2019, 2020]
  length = [103, 101, 99, 100, 100, 95, 95, 96, 93, 90]

  for i in range(len(years)):
    ret[years[i]] = length[i]
